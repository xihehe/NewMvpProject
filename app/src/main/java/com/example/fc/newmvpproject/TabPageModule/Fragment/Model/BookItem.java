package com.example.fc.newmvpproject.TabPageModule.Fragment.Model;

public class BookItem  {

    String book_name;
    String book_img;
    String book_tips;
    String book_txt;

    public String getBook_name() {
        return book_name;
    }

    public void setBook_name(String book_name) {
        this.book_name = book_name;
    }

    public String getBook_img() {
        return book_img;
    }

    public void setBook_img(String book_img) {
        this.book_img = book_img;
    }

    public String getBook_tips() {
        return book_tips;
    }

    public void setBook_tips(String book_tips) {
        this.book_tips = book_tips;
    }

    public String getBook_txt() {
        return book_txt;
    }

    public void setBook_txt(String book_txt) {
        this.book_txt = book_txt;
    }
}
